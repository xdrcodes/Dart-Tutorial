void main() {
  // ITERATORS: SYNTAX
  
  //For Loop:
  for(var i = 0; i < 4; i++) {
    print("For Loops");
  }
  print("");
  //While Loop:
  var j = 0;
  while(j < 4) {
    print("While Loops");
    j++;
  }
  print("");
  //Do While Loop:
  var l = 0;
  do {
    print("Do While Loops");
    l++;
  } while(l < 4);
}